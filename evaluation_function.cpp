#include "evaluation_function.h"

int EvaluationFunction::evaluate(Board& board, MoveGenerator& moveGenerator, const int side) {
    return getMaterialBalance(board);
}

int EvaluationFunction::getMaterialBalance(Board& board) {
    int materialBalance = 0;

    for (int p = 0; p < 6; p++) {
        materialBalance += getPieceBalance(p, board.pieces[p][0], board.pieces[p][1]);
    }
    return materialBalance;
}

int EvaluationFunction::getPieceBalance(const int index, const U64 whiteBB, const U64 blackBB) {
    U64 b = whiteBB;
    int balance = 0;

    while (b > 0) {
        balance += (Board::PIECE_VALUES[index] + PIECE_SQUARE_VALUES[index][Bit::Pop(b)]);
    }
    b = blackBB;
    while (b > 0) {
        balance -= (Board::PIECE_VALUES[index] + PIECE_SQUARE_VALUES[index][63-Bit::Pop(b)]);
    }
    return balance;
}

